/// Toggle size variants
enum ToggleSize {
  /// Small Toggle - compact size
  sm,

  /// Medium Toggle - default size
  md,

  /// Large Toggle - expanded size
  lg,
}

/// Toggle visual states (for styling)
enum ToggleVisualState {
  /// Normal state - default appearance
  normal,

  /// Focused state - when toggle has keyboard focus
  focused,

  /// Disabled state - when toggle is not interactive
  disabled,
}
